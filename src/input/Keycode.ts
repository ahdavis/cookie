/**
 * An input keycode
 */
export enum Keycode {
	K1 = 0x0, //numeric 1 key
	K2 = 0x1, //numeric 2 key
	K3 = 0x2, //numeric 3 key
	K4 = 0x3, //numeric 4 key
	KQ = 0x4, //Q key
	KW = 0x5, //W key
	KE = 0x6, //E key
	KR = 0x7, //R key
	KA = 0x8, //A key
	KS = 0x9, //S key
	KD = 0xA, //D key
	KF = 0xB, //F key
	KZ = 0xC, //Z key
	KX = 0xD, //X key
	KC = 0xE, //C key
	KV = 0xF, //V key
	KNULL = 0x10 //null key
}

